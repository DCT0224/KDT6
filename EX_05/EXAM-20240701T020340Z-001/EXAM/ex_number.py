# ----------------------------------------------------
# 숫자 표현 관련 함수 살펴보기
# ----------------------------------------------------
# 숫자 표현 방법 => 2진수, 8진수, 16진수
# 2진수 : 0b1010
# 8진수 : 0o1234
# 16진수: 0x1234
# 10진수 :  1234
# ----------------------------------------------------

# 10진수 => 2진수로 변환 함수 bin(정수)
print( 5, bin(5))

# 10진수 => 16진수로 변환 함수 hex(정수)
print(21, hex(21))

# 10진수 => 8진수로 변환 함수 oct(정수)
print(9, oct(9))
